class EDA:
    def __init__(self, file):
        self.df = pd.read_csv(file)
        self.clean_data()

    def clean_data(self):
        self.df.replace('-', 0, inplace=True)
        self.df[['ì¸êµ¬', 'ì¶œìƒì•„ìˆ˜(ëª…)', 'ì‚¬ë§ììˆ˜(ëª…)']] = self.df[['ì¸êµ¬', 'ì¶œìƒì•„ìˆ˜(ëª…)', 'ì‚¬ë§ììˆ˜(ëª…)']].apply(pd.to_numeric)
        self.df.fillna(0, inplace=True)

    def show(self):
        tabs = st.tabs(["ê¸°ì´ˆ í†µê³„", "ì—°ë„ë³„ ì¶”ì´", "ì§€ì—­ë³„ ë¶„ì„", "ë³€í™”ëŸ‰ ë¶„ì„", "ì‹œê°í™”"])

        with tabs[0]:
            st.subheader("ğŸ“Š ê¸°ë³¸ í†µê³„ ë° êµ¬ì¡°")
            st.write("ë°ì´í„°í”„ë ˆì„ ì •ë³´:")
            buffer = self.df.info(buf=None)
            st.text(str(buffer))
            st.write("ê¸°ìˆ  í†µê³„:")
            st.write(self.df.describe())

        with tabs[1]:
            st.subheader("ğŸ“ˆ ì „ì²´ ì¸êµ¬ ì—°ë„ë³„ ì¶”ì´")
            national = self.df[self.df['ì§€ì—­'] == 'ì „êµ­']
            fig, ax = plt.subplots()
            ax.plot(national['ì—°ë„'], national['ì¸êµ¬'], marker='o', label='Actual')

            # ë¯¸ë˜ ì¸êµ¬ ì˜ˆì¸¡ (ë‹¨ìˆœ ì„ í˜• ì¶”ì •)
            recent = national.tail(3)
            mean_births = recent['ì¶œìƒì•„ìˆ˜(ëª…)'].mean()
            mean_deaths = recent['ì‚¬ë§ììˆ˜(ëª…)'].mean()
            projected_growth = mean_births - mean_deaths
            pop_2035 = national['ì¸êµ¬'].iloc[-1] + projected_growth * (2035 - national['ì—°ë„'].iloc[-1])
            ax.plot(list(national['ì—°ë„']) + [2035], list(national['ì¸êµ¬']) + [pop_2035], linestyle='--', marker='x', color='red', label='Projected')

            ax.set_title('Population Trend')
            ax.set_xlabel('Year')
            ax.set_ylabel('Population')
            ax.legend()
            st.pyplot(fig)

        with tabs[2]:
            st.subheader("ğŸ“ ìµœê·¼ 5ë…„ê°„ ì§€ì—­ë³„ ì¸êµ¬ ë³€í™”")
            latest_year = self.df['ì—°ë„'].max()
            past_year = latest_year - 5

            pop_change = self.df[self.df['ì—°ë„'].isin([past_year, latest_year])]
            pivot = pop_change.pivot(index='ì§€ì—­', columns='ì—°ë„', values='ì¸êµ¬')
            pivot['Change'] = pivot[latest_year] - pivot[past_year]
            pivot['Rate'] = ((pivot['Change'] / pivot[past_year]) * 100).round(2)
            pivot = pivot.drop(index='ì „êµ­').sort_values('Change', ascending=False)

            # ì§€ì—­ëª…ì„ ì˜ì–´ë¡œ ë³€í™˜ (ì˜ˆì‹œìš© ë§¤í•‘)
            region_map = {region: f"Region_{i}" for i, region in enumerate(pivot.index)}
            pivot['Region'] = pivot.index.map(region_map)

            fig, ax = plt.subplots(figsize=(8, 6))
            sns.barplot(y='Region', x='Change', data=pivot, ax=ax, palette='viridis')
            ax.set_title('Population Change (5 years)')
            ax.set_xlabel('Change (in thousands)')
            ax.set_ylabel('')
            for i, v in enumerate(pivot['Change']):
                ax.text(v, i, f'{int(v / 1000)}K', color='black', va='center')
            st.pyplot(fig)

            fig2, ax2 = plt.subplots(figsize=(8, 6))
            sns.barplot(y='Region', x='Rate', data=pivot, ax=ax2, palette='coolwarm')
            ax2.set_title('Population Growth Rate (%)')
            ax2.set_xlabel('Growth Rate')
            ax2.set_ylabel('')
            for i, v in enumerate(pivot['Rate']):
                ax2.text(v, i, f'{v:.1f}%', color='black', va='center')
            st.pyplot(fig2)

            st.markdown("**Interpretation:** These graphs show the absolute and percentage population changes by region over the past 5 years.")

        with tabs[3]:
            st.subheader("ğŸ“ˆ ì—°ë„ë³„ ì§€ì—­ ì¸êµ¬ ì¦ê° Top 100")
            df_diff = self.df.copy()
            df_diff['Change'] = df_diff.sort_values(['ì§€ì—­', 'ì—°ë„']).groupby('ì§€ì—­')['ì¸êµ¬'].diff()
            top100 = df_diff[df_diff['ì§€ì—­'] != 'ì „êµ­'].sort_values('Change', ascending=False).head(100)
            top100_display = top100[['ì—°ë„', 'ì§€ì—­', 'Change']].copy()
            top100_display['Change'] = top100_display['Change'].apply(lambda x: f"{int(x):,}")
            st.dataframe(
                top100_display.style.background_gradient(
                    subset=['Change'], cmap='RdBu', axis=0
                )
            )

        with tabs[4]:
            st.subheader("ğŸ“Š ëˆ„ì  ì˜ì—­ ê·¸ë˜í”„ (ì§€ì—­ë³„ ì—°ë„ë³„ ì¸êµ¬)")
            pivot_area = self.df.pivot(index='ì—°ë„', columns='ì§€ì—­', values='ì¸êµ¬').fillna(0)
            pivot_area = pivot_area.drop(columns='ì „êµ­', errors='ignore')
            region_map = {region: f"Region_{i}" for i, region in enumerate(pivot_area.columns)}
            pivot_area.rename(columns=region_map, inplace=True)

            fig, ax = plt.subplots(figsize=(10, 6))
            pivot_area.div(1000).plot.area(ax=ax, cmap='tab20')
            ax.set_title('Population by Region (Cumulative)')
            ax.set_xlabel('Year')
            ax.set_ylabel('Population (Thousands)')
            st.pyplot(fig)


